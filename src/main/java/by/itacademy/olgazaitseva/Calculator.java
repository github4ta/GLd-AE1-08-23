public class Calculator {
    public static int add(int a, int b) {
        return (a + b);
    }

    public static int multiply(int a, int b) {
        return (a * b);
    }
    public static int deduct(int a, int b) {

        return (a - b);
    }
    public static int divide(int a, int b) {
        return (a / b);
    }
}
