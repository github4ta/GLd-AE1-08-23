package by.itacademy.aksanabazhenava;

public class Calculator {

    public static int add(int a, int b) {
        int addResult;
        addResult = a + b;
        return addResult;
    }

    public static int multiplay(int a, int b) {
        int multiplaResult;
        multiplaResult = a * b;
        return multiplaResult;
    }
    public static int deduction(int a, int b){
        return (a - b);
    }

    public static int division(int a, int b){
        return (a / b);
    }
}
