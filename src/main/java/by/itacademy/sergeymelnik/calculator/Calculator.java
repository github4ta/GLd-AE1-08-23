public class Calculator {
    public static int add(int a, int b){
        return a+b;
    }
    public static int multiply(int a, int b){
        return a*b;
    }
    public static int deviding(int a, int b){
        return a/b;
    }
    public static int subtraction(int a, int b){
        return a-b;
    }

}
